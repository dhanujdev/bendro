/**
 * Pick the right pain-prompt disclaimer for a 0–10 pain rating.
 * Thresholds mirror `src/services/safety.ts::classifyPainRating`.
 */
export function painPromptForRating(
  rating: number,
): Disclaimer {
  if (rating >= 7) return DISCLAIMERS.painPromptHigh
  if (rating >= 4) return DISCLAIMERS.painPromptMedium
  return DISCLAIMERS.painPromptLow
}

/**
 * Convenience lookup — used by the <DisclaimerBanner/> component and by
 * audit tooling that walks the surface list at test time.
 */
export function getDisclaimer(surface: DisclaimerSurface): Disclaimer {
  return DISCLAIMERS[surface]
}
