/**
 * Single source of truth for every user-facing disclaimer mandated by
 * `.claude/rules/HEALTH_RULES.md §Mandatory Disclosures`.
 *
 * Every surface that renders health-adjacent copy MUST import from here.
 * Inline health copy anywhere else in the tree is a rule violation that
 * `pr-reviewer` will flag.
 *
 * When adding a new surface, also add it to `DISCLAIMER_SURFACES` so the
 * test in `tests/unit/disclaimers.test.ts` fails if the surface type has
 * no copy wired up.
 */

export type DisclaimerSurface =
  | "onboardingIntro"
  | "routineStart"
  | "painPromptLow"
  | "painPromptMedium"
  | "painPromptHigh"
  | "safetyGate"
  | "aiGeneratedCard"
  | "marketingPainRelief"

export interface Disclaimer {
  /** Stable id for audit logging + testing. */
  id: DisclaimerSurface
  /** The copy rendered to the user. */
  body: string
  /**
   * Optional CTA link. When present the renderer should show it as a
   * button/link beneath the body.
   */
  cta?: {
    label: string
    /** Always a route on our domain — never an external URL. */
    href: string
  }
  /** Severity drives the visual treatment (neutral / caution / warn). */
  severity: "neutral" | "caution" | "warn"
}

